// Defining function for addition
function displaySum(num1, num2) {
    var total = num1 + num2;
    document.write(total);
}
     
// Calling function
displaySum(6, 20); // Prints: 26
document.write("<br />");
displaySum(-5, 17); // Prints: 12
document.write("<br />");
displaySum(100, 17); // Prints: 117